class Solution:
    def depthSumInverse(self, nestedList: List[NestedInteger]) -> int:
        def dfs(cur):
            d = 0
            for ne in cur.getList():
                d = max(d, dfs(ne))
            return d+1
        max_depth = max([dfs(cur) for cur in nestedList])
        res = 0
        def dfs1(cur, d):
            nonlocal res
            for ne in cur.getList():
                dfs1(ne, d+1)
            if cur.getInteger():
                res += cur.getInteger() * (max_depth - d + 1)
        for cur in nestedList:
            dfs1(cur, 1)
        return res